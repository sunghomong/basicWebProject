package com.green.vo;

public class GradeAvgVO {
	
	private String username;
    private float kor_avg;
    private float eng_avg;
    private float math_avg;
    private float sci_avg;
    private float sc_avg;
    
    
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public float getKor_avg() {
		return kor_avg;
	}
	public void setKor_avg(float kor_avg) {
		this.kor_avg = kor_avg;
	}
	public float getEng_avg() {
		return eng_avg;
	}
	public void setEng_avg(float eng_avg) {
		this.eng_avg = eng_avg;
	}
	public float getMath_avg() {
		return math_avg;
	}
	public void setMath_avg(float math_avg) {
		this.math_avg = math_avg;
	}
	public float getSci_avg() {
		return sci_avg;
	}
	public void setSci_avg(float sci_avg) {
		this.sci_avg = sci_avg;
	}
	public float getSc_avg() {
		return sc_avg;
	}
	public void setSc_avg(float sc_avg) {
		this.sc_avg = sc_avg;
	}
	

    
    
}
